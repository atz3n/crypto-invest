#!/bin/bash

# Runs the docker-compose project

###################################################################################################
# CONFIGURATION
###################################################################################################

PROJECT_NAME="market-cap-weighting"

COMPOSE_FILE="docker-compose-finite.yml"
# COMPOSE_FILE="docker-compose-infinite.yml"


###################################################################################################
# DEFINES
###################################################################################################

HERE="$(pwd)/$(dirname $0)"


###################################################################################################
# MAIN
###################################################################################################

SUDO=""
if [ $(uname) == Linux ]; then
    SUDO="sudo"
fi

cd ${HERE}/../docker
${SUDO} docker-compose -p ${PROJECT_NAME} -f ${COMPOSE_FILE} up -d